import { createElement } from 'react';
import { renderToStaticMarkup } from 'react-dom/server';
import { describe, expect, it } from 'vitest';

import { WorkflowDeliverableTargetLink } from './workflow-deliverable-target-link.js';

describe('WorkflowDeliverableTargetLink', () => {
  it('renders workflow-context targets inline instead of linking to deprecated task or workflow pages', () => {
    const taskHtml = renderToStaticMarkup(
      createElement(WorkflowDeliverableTargetLink, {
        target: {
          target_kind: 'task',
          label: 'Generated by task',
          url: '/work/tasks/task-1',
        },
      }),
    );
    const workflowHtml = renderToStaticMarkup(
      createElement(WorkflowDeliverableTargetLink, {
        target: {
          target_kind: 'workflow',
          label: 'Workflow summary',
          url: '/workflows/workflow-1?tab=details',
        },
      }),
    );

    expect(taskHtml).toContain('Generated by task (Task)');
    expect(taskHtml).toContain('Reference stays in this workflow workspace.');
    expect(taskHtml).not.toContain('href="/work/tasks/task-1"');
    expect(workflowHtml).toContain('Workflow summary (Workflow)');
    expect(workflowHtml).toContain('Reference stays in this workflow workspace.');
    expect(workflowHtml).not.toContain('href="/workflows/workflow-1?tab=details"');
  });
});
